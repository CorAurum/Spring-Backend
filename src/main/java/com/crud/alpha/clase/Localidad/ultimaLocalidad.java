package com.crud.alpha.clase.Localidad;

import com.crud.alpha.clase.Omnibus.Omnibus;
import jakarta.persistence.*;
import lombok.*;
import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.jpa.domain.support.AuditingEntityListener;

import java.time.LocalDate;
import java.time.LocalDateTime;
import java.time.LocalTime;

@Entity
@EntityListeners(AuditingEntityListener.class)
@Table(name = "ultimaLocalidad")
@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class ultimaLocalidad {
// Importante no nombrar atributos utilizando '_' (piso/barra baja)

    // *** Autogenerated
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    // *** Manual
    @Column(nullable = false)
    private LocalDate fecha;

    @Column(nullable = false)
    private LocalTime hora;

    @CreatedDate
    private LocalDateTime createdAt;

    @LastModifiedDate
    private LocalDateTime updatedAt;

    // *** FK
    @ManyToOne
    private Omnibus omnibus;

    @ManyToOne
    @JoinColumn(name = "localidad_nombre", referencedColumnName = "nombre")
    private Localidad localidad;

}